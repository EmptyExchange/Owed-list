//pulls data from text boxes when received button is clicked
function receivedFunction() {
    const inputProduct = document.getElementById('product').value;
    const inputQuantity = document.getElementById('quantity').value;
    const productLines = inputProduct.split('\n');
    const quantityLines = inputQuantity.split('\n');
    let quantityArray = quantityLines.map(Number);
 for(let i = 0; i < productLines.length; i++){
    //checks the product number entered is in the willNumLookup object
    if(wilNumLookup[productLines[i]]) {
      showItems.push(wilNumLookup[productLines[i]]);       
      } else { 
      
       
        alert(productLines[i] + " NOT FOUND")
       

      }
  }
    //checks found product against localStorage
  for (let i = 0; i < showItems.length; i++) {
    const foundValue = JSON.parse(localStorage.getItem(showItems[i]));
    if (localStorage.key(showItems[i])){
         //if product is found in localStorage it is removed and replaced with calculated value 
         let newLocalValue = foundValue - quantityArray[i];
          localStorage.removeItem(showItems[i]);
          localStorage.setItem(showItems[i], newLocalValue)
        } else {
          //product is not found so is added to localStorage as a negative value
          localStorage.setItem(showItems[i], 0 - quantityArray[i]);
        } 
          //product value showing zero is removed
    if (JSON.parse(localStorage.getItem(showItems[i])) === 0) {
          localStorage.removeItem(showItems[i])
        } 
  }
  location.reload()
}


